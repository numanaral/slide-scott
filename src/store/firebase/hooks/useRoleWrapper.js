import useNotificationProvider from 'store/redux/hooks/useNotificationProvider';
import useRoles from './useRoles';

const useRoleWrapper = () => {
	const { isPaidUser } = useRoles();

	const { notifyWarning } = useNotificationProvider();

	const wrapPaidFeature = cb => {
		if (!isPaidUser) {
			notifyWarning('Paid feature.');
			return;
		}
		cb();
	};

	return wrapPaidFeature;
};

export default useRoleWrapper;
